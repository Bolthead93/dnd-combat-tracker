def make_combat_title(title_name=""):
    word_to_make = [letter.lower() for letter in title_name if letter.isalpha() or letter == " "]

    # give me the height of ascii characters based on a
    ascii_height = len(ascii_letters["a"].split("\n")) - 1

    word_as_list = []
    # for each row from the height
    for row in range(ascii_height):
        # take the letter and it's related row
        word_as_list.append([])
        for letter in word_to_make:
            letter_as_row = ascii_letters[letter].split("\n")
            word_as_list[row].append(letter_as_row[row])
    for row in word_as_list:
        if len(row) > 0:
            print(str("").join(row))


def create_banner(text):
    banner_length = len(section_spaces)
    word_length = len(text)
    insert_location = int(banner_length/2) - int(word_length/2) - 1
    insert_text = section_spaces[:insert_location] + text + section_spaces[insert_location+word_length:]
    return f"{section_split}\n{insert_text}\n{section_split}"


